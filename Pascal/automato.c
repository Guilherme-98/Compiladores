// Acrescenta a linha ou a coluna para +1 dependendo do caractere lido
// Caso o caractere seja um \n, reseta a coluna para 0
void calcular_linha_coluna(char caract_lido) {
	switch (caract_lido){
		case 9: 
			coluna_acrescentar(); 
			break;
		case 10: 
			coluna_zerada();
			linha_acrescentar();
			break;
		case 32:
			coluna_acrescentar();
			break;
	}
}

