int get_estado_prox(int estado, char caract_lido) {
	// Comentario (**) - simbolo "*"
	if(estado == 77) {
		if(caract_lido == 42) {
			return 78;
		}
		else if(caract_lido == 10) {
			linha_acrescentar();
			coluna_zerada();
		}
		return 77;
	}

	// Comentario (**) - simbolo ")"
	else if(estado == 78) {
		if(caract_lido == 41) {
			return 79;
		}
		else if(caract_lido == 10) {
			linha_acrescentar();
			coluna_zerada();
		}
		return 77;
	}

	// Comentario {} - simbolo "}"
	else if(estado == 80) {
		if(caract_lido == 125) {
			return 79;
		}
		else if(caract_lido == 10) {
			linha_acrescentar();
			coluna_zerada();
		}
		return 80;
	}

	else {
		if(caract_lido > 96 && caract_lido < 123) {
			return transicoes[estado][(get_indice(toupper(caract_lido)))];
		}
		else if(get_indice(caract_lido) == -1) {
			return 0;
		}
		return transicoes[estado][(get_indice(caract_lido))];
	}
}

